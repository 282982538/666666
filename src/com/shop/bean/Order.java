package com.shop.bean;

public class Order {
	private int oid;
	private int uid;
	private int is;
	private String time;
	private int cis;
	
	public int getCis() {
		return cis;
	}
	public void setCis(int cis) {
		this.cis = cis;
	}
	public int getOid() {
		return oid;
	}
	public void setOid(int oid) {
		this.oid = oid;
	}
	public int getUid() {
		return uid;
	}
	public void setUid(int uid) {
		this.uid = uid;
	}
	public int getIs() {
		return is;
	}
	public void setIs(int is) {
		this.is = is;
	}
	public String getTime() {
		return time;
	}
	public void setTime(String time) {
		this.time = time;
	}
	
	
}
